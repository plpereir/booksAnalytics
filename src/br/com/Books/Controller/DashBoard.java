package br.com.Books.Controller;

public class DashBoard {
	private String item;
	private Float ratingTotal;
	private Float ratingAverage;
	private Float ratingPerc;
	private String seller;
	private String sellerprice;
	private String sellerrating;
	
	/**
	 * @return the item
	 */
	public String getItem() {
		return item;
	}
	/**
	 * @param item the item to set
	 */
	public void setItem(String item) {
		this.item = item;
	}
	/**
	 * @return the ratingTotal
	 */
	public Float getRatingTotal() {
		return ratingTotal;
	}
	/**
	 * @param ratingTotal the ratingTotal to set
	 */
	public void setRatingTotal(Float ratingTotal) {
		this.ratingTotal = ratingTotal;
	}
	/**
	 * @return the ratingAverage
	 */
	public Float getRatingAverage() {
		return ratingAverage;
	}
	/**
	 * @param ratingAverage the ratingAverage to set
	 */
	public void setRatingAverage(Float ratingAverage) {
		this.ratingAverage = ratingAverage;
	}
	/**
	 * @return the ratingPerc
	 */
	public Float getRatingPerc() {
		return ratingPerc;
	}
	/**
	 * @param ratingPerc the ratingPerc to set
	 */
	public void setRatingPerc(Float ratingPerc) {
		this.ratingPerc = ratingPerc;
	}
	/**
	 * @return the seller
	 */
	public String getSeller() {
		return seller;
	}
	/**
	 * @param seller the seller to set
	 */
	public void setSeller(String seller) {
		this.seller = seller;
	}
	/**
	 * @return the sellerprice
	 */
	public String getSellerprice() {
		return sellerprice;
	}
	/**
	 * @param sellerprice the sellerprice to set
	 */
	public void setSellerprice(String sellerprice) {
		this.sellerprice = sellerprice;
	}
	/**
	 * @return the sellerrating
	 */
	public String getSellerrating() {
		return sellerrating;
	}
	/**
	 * @param sellerrating the sellerrating to set
	 */
	public void setSellerrating(String sellerrating) {
		this.sellerrating = sellerrating;
	}
}
